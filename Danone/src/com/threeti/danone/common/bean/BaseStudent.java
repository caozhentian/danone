package com.threeti.danone.common.bean;

public class BaseStudent {

	protected Long id     ;
    /** Not-null value. */
	protected String name ;
	protected int age     ;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
